 	Quem Somos?
	Sonho do casal de imigrantes portugueses, que ja nao viam mais futuro na sua freguesia de doces feita no caminhão de<br> doces Bela Vista. Procuraram então, uma alternativa de negócio. Depois de muita ajuda e esforço, em Outubro de 1984, fundaram a Malu,<br>  
	um estabelecimento alugado, com pouco mais de 80m2, um funcionário e quatro filhos, foram firmando seu espaço.<br><br>

Doce Malu em 1984<br>
Sempre preocupados em servir bem, traziam sempre novidades e pedidos dos clientes. Pouco a pouco a Malu foi crescendo, e o espaço inicial, <br> 
tornou-se pequeno para tanta variedade. Precisaram de um novo espaço, muito maior que o primeiro, mudaram-se  então para o prédio proprio<br>
  com 600m2, montando uma loja moderna, com auto-serviço e assim, criando sua própria identidade: um supermercado de Doces.<br><br>

Grande e informatizada, mas preservando o espírito familiar, a Malu foi crescendo, e assim continua até hoje em sua expansão, seja no espaço <br>

físico da loja, hoje com 1200m2, seja no espaço cibernético com a inauguração da loja virtual.Então, se precisar de um docinho pra comer em casa, <br>
de produtos para sua padaria, lanchonete, cantina, cafeteria, de materiais para chocolate caseiro, cestas de café da manha, sorveteria e confeitaria,<br>
 de embalagens diversas, de artigos para a festa de seus filhos, com preço justo e confiança, visite nossas seções.
